<h1 class="weeds-font">The Weeds You Have in Your Mind</h1>
<div class="story">
    <p><span class="weeds-font">T</span>here was the sound of birds singing, and of children laughing in the distance. There was the reassuring hum of the air conditioning unit. The wind carried a world of sounds confirming the truth that no sound is better than another. A sound comes, is heard, and then is gone. It appears and disappears like a wisp of a cloud momentarily drawn in the sky. Nothing is left in its wake. Nothing ever happened. Nothing ever lasts. The only thing that lasts is everything. Everything is now. It happens above, below, inside, and all around. Within and without. Jessica thought of this as she sat on a pillow on the porch and felt the breeze in her hair.
    </p><p>
        "Now open your eyes," the voice said. "Congratulations for keeping up with your practice. I hope to see you tomorrow. Stay mindful."
    </p><p>
        Jessica thought she was making wonderful progress. Ever since she downloaded a guided meditation application for her phone, everything just seemed so clear. The here and now is all that really matters. She felt as if she had been handed the key to life. It was all so simple. She told everyone.
    </p><p>
        "You don't understand. It like, really helps you put things into perspective and get on with your life, you know?" She had explained to her internet friend Matthew. "It just helps you cut through all the noise."
    </p><p>
        "What?" Matthew asked.
    </p><p>
        "Horizons. The app. You need to get it."
    </p><p>
        "My phone is so slow, it can't fit anything else," he said. "You know I have four dating apps? Seriously, and they all suck. This same creepy old dude keeps messaging me on all of them, I'm not even joking."
    </p><p>
        Jessica rolled her eyes. "You're so negative. I'm sure he's a very nice person. We should all be connecting. This is why you need to meditate."
    </p><p>
        With mindfulness comes patience, understanding, and compassion for others. It said so in some of the reading material for the app, and Jessica agreed. As her practice became more regular, she felt as if she was in a place of centeredness all throughout the day. If her focus temporarily waned, she would catch herself and bring it back to awareness.
    </p><p>
        There were trying times however. Work in particular often threatened to shatter her calm. Customers could be bewildering.
    </p><p>
        "I ain't ordered this," such a person said to Jessica one evening, while pointing at a side of mashed potatoes.
    </p><p>
        "I'm sorry?" She replied.
    </p><p>
        "I ain't ordered this, and I ain't paying for it neither, I'm sorry honey."
    </p><p>
        Jessica distinctly remembered the woman ordering the side dish.
    </p><p>
        "Oh, wow. Let's take a deep breath. My apologies, I'll get it out of your way."
    </p><p>
        There were 31 steps back to the kitchen. She followed the movements of her breath along the way as she carried the food. Five deep breaths, full of potatoes. Everyone should periodically take the time to notice their surroundings. The restaurant was busy and loud with a sustained drone of indistinct chatter. One had to be on the constant lookout not to bump into small children running between the tables, threatening disaster.
    </p><p>
        She told her manager what happened.
    </p><p>
        "Did they order the potatoes?" he asked.
    </p><p>
        "I think so," Jessica replied.
    </p><p>
        "You screw up sometimes," he said. It was true. In that moment she hated him. But hate was not the answer.
    </p><p>
        "Don't screw up again, please."
    </p><p>
        On the way home she stopped at a gas station to buy a pack of cigarettes. The clerk did not ask to see her ID, which made her worried. In the car she smoked. A whole four days had passed since she had last done this, and so it burned her throat more than expected. The burning went away though and she felt relaxed as she drove through the dark.
    </p><p>
        At home she felt distressed. Four days' progress gone. Her poor lungs. All the cigarettes went in the trash, except for one, for just before bed. It was really time to quit. She heated up a frozen meal, poured a glass of wine, and turned her thoughts to the future. No, not the future. The present moment. The food wasn't bad. She was a healthy girl and she had an apartment. There was absolutely nothing wrong in the universe. The world would be so different if everyone could realize this.
    </p><p>
        The next morning she awoke with the sun shining on her face. Her head hurt. She rose from the couch and dug a cigarette out of the trash. There was a shattered wine bottle on the kitchen floor.
    </p><p>
        "Well, shit," she said to no one, exhaling smoke.
    </p><p>
        Her phone began humming somewhere in the couch. She dug through the cushions and found it. The vibration was due to a daily alarm she had set. This meant that it was time for morning meditation.
        "Shit," she said again.
    </p><p>
        After a cup of green tea and another smoke, Jessica was on the porch sitting on her cushion in the half-lotus position. She pulled up the meditation application on her phone and set the device in front of her.
    </p><p>
        "Hello again," the voice said. "Today, let's try sitting for 15 minutes. Remember, focus on your breathing. If you experience pain or discomfort, take notice, observe the discomfort. If your mind wanders, gently bring it back to the breath, never judging, always accepting. There is no wrong path. Let's begin."
    </p><p>
        Fifteen minutes would be tough. She closed her eyes and breathed deeply. In breath. Out breath. In….and out. She heard the buzzing of a leaf blower in the distance. Her leg felt a bit uncomfortable. It would pass eventually. She took notice. Sounds, feelings, thoughts… These things are ephemeral. There really weren't any legs attached to her at all. There was no her.
    </p><p>
        She was a mountain. Thoughts came in like clouds, but a mountain doesn't care about clouds. The mountain persists forever. Or until it erodes. Then its dirt blows away, settling in a far away land, to lie quietly until the earth shifts and it finds itself lifted to the precipice of a brand new peak, touching the sky.
    </p><p>
        Jessica did not want to erode. But she remembered the breath. Her leg did not hurt anymore. The leaf blower was still roaring. One time, she had read an article or something about how so many insects nest and hibernate in the leaves during the chilly season. When people rake or blow leaves it upsets the cycle, probably killing untold generations. It was sad. Why couldn't people just let the leaves fall where they may? Why couldn't they stay there? She listened deeply to the sound for an answer.
    </p><p>
        For a while she felt close to learning something, but then her cheek started to tingle. It soon grew quickly into a more desperate itching. She knew she should ignore it. She really tried to let it go, but suddenly the horrible realization came that it was certainly a bug crawling on her face. Before she could think, she raised her hand and swatted it. Whatever it was felt solid, and then wet. She opened her eyes and looked down at the back of her hand to see a smashed and broken beetle. Jessica groaned and stormed into the house, just as the ending chime of the meditation bell rang out.
    </p><p>
        The rest of her afternoon was spent on the couch. She watched television and painted her nails. She read a lengthy article on her phone about the importance of antioxidants, which could be found in abundance in blueberries or cranberries. Eventually a text message arrived from her friend Charlotte, inviting her out for drinks that evening. It sounded fun.
    </p><p>
        Five of them were packed into a taxi, headed to the club. They passed a bottle of something around and around, each taking a gulp.
    </p><p>
        "You can't drink in here," the driver said. "It's against the law."
    </p><p>
        "We'll finish it," Jessica said.
    </p><p>
        "Yeah, finish it Jess," Charlotte said. They all laughed.
    </p><p>
        Her stomach burned and rumbled as she stepped out of the car. She thought she shouldn't drink much more or might risk getting sick, but felt better as they waited in line to get in the door. Charlotte grasped her hand and led her inside through the cloud of cigarette smoke. They weaved through the tight gaps in the crowd in the general direction of the bar. After a very long wait the bartender nodded for her order.
    </p><p>
        "I'll take a vodka and cranberry." Antioxidants!
    </p><p>
        Two hours later, Jessica was out on the back patio attempting to demonstrate how to blow smoke rings. The guy she was teaching wasn't there anymore though. She turned to a new acquaintance and asked them if they had danced. They said no.
    </p><p>
        "I never, ever dance," Jessica explained. "Unless the music is just really good. I really liked that band. They had a theremin, that's so cool."
    </p><p>
        "What's that?" The person asked.
    </p><p>
        "It's that instrument you play with your hands like this." Jessica twirled her hands in the air.
    </p><p>
        "Oh. I'll see you later."
    </p><p>
        Her drink was almost empty. She tried to dig the lime wedge out of the ice, but ended up dropping the whole thing. She told the people around her that she was sorry. Charlotte came and led her to a bench in the back.
    </p><p>
        "What's wrong with you tonight? You're acting like a fucking mess, it's embarrassing," Charlotte said.
    </p><p>
        Jessica told her only friend that she loved her and thought about her all the time.
    </p><p>
        "Oh my god. Don't say that. You need to go home."
    </p><p>
        Maybe she was right. Friends sucked. People were so hard to deal with. It's hard enough to deal with yourself. She ordered another drink and went to the bathroom.
    </p><p>
        Inside the stall, she sat on the toilet and sipped her drink while looking at her phone. Tomorrow she was going to apply to go back to college. It was her dream to be a nutritionist, maybe with a minor in philosophy. She wanted to start taking yoga classes too. That kind of thing can really help you get your life together. She didn't have any money though. That would probably work itself out. Things always work themselves out. You just have to go with the flow.
    </p><p>
        "Are you still in here?" Two drunk girls in high heels and miniskirts barged into the stall.
    </p><p>
        "Excuse me! Are you kidding me?" Jessica said.
    </p><p>
        "Jesus we need to pee," one of them said, still standing there.
    </p><p>
        "Did you just fucking come in here while I'm in here? What's wrong with you?"
    </p><p>
        "Oh my god. Hurry up chick, are you taking a shit?" They laughed at her.
    </p><p>
        Jessica, rising from the toilet, took her drink and threw it at one of their faces. It hit its mark. Everyone seemed shocked for a moment at what had just happened. Then the other girl then came at her, swinging her purse. In a moment, all three of them were on the floor of the stall, tangled together. None of them could seem to get up. Jessica attempted to scoop up the ice from the disgusting floor and smear it on the other girls' clothes.
    </p><p>
        The bouncer came in and unraveled them. Jessica explained that they had basically assaulted her. He didn't seem interested in the truth. She was brought outside by the arm and told to leave.
    </p><p>
        "I don't believe it," she said. It was hard for her to stand up, so she sat down on the curb and rested her head in her hands.
    </p><p>
        The next day she found herself on her porch, lying on the door mat. It took a while to try to put the pieces together, and some were definitely missing, along with her phone and her wallet. At least her keys were still in her pocket. She went inside and looked at the time. Almost noon. Almost an hour late.
    </p><p>
        There was nothing to be done, except throw on some new pants and her work shirt, and rush as fast as she could. Traffic was inexplicably bad. It made no sense at all. She shouted and screamed and cried the whole way there. Her head hurt worse than it ever had before. At a red light, she opened her door and vomited out the side of her car. Other drivers began honking and went around her when the light changed. Jessica shrank in her seat, mortified and shaking.
    </p><p>
        An eternity later, she finally made it there. She cleaned herself up in the rear view mirror as best as she could before heading inside and preparing a story for her manager. He was in the kitchen.
    </p><p>
        "Richie, I'm so sorry," she said. "I lost my phone and my wallet and my best friend. I had the worst night ever."
    </p><p>
        He put his finger in the air to cut her off. "I already called in Stephanie. I'm sorry. You should just go home. You can, you know, pick up your check on Friday."
    </p><p>
        "No, please, you don't understand."
    </p><p>
        He shook his head. "It's the last time. Did you really think you were gonna work today anyway? You look terrible. It's super unattractive."
    </p><p>
        For a while she stood there. Her eyes were starting to water again.
    </p><p>
        "How am I going to pay for school?" She asked, looking down. He shrugged and walked away.
    </p><p>
        Jessica turned around. Every fiber of her tensed up and began to panic. She let out a brief, involuntary scream. A million different awful, life-destroying scenarios raced through her brain in the span of seconds. The world was hopelessness and despair. She couldn't keep a grip on anything. She never could, not once in her life, and so she decided to let go.
    </p><p>
        She began walking. She went through the crowded dining area, where people held their forks motionless and watched, murmuring to each other, asking do you see that crazy girl? But her mind was empty now.
    </p><p>
        Actually, she was a bit tired. It was a good time to sit down and rest. She removed her shoes and sat down cross-legged on the restaurant floor. She closed her eyes and took a deep breath. The air felt nice as it moved in and out of her. It came and went, flowing like water, just like it always had. Everything about it was comforting and beautiful. How did she never notice it before? She paid it complete attention, unconcerned as the passing time stretched into hours and days and eons, and her body became dust and flew away with the wind.
    </p>
